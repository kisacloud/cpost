#!/bin/bash

# =========================================================
# CPOST 배포 자동화 스크립트
# =========================================================

echo "🚀 [1/3] 최신 코드로 도커 이미지를 빌드합니다..."
docker-compose build

echo "♻️ [2/3] 이전 컨테이너를 제거하고 새 컨테이너를 가동합니다..."
# --remove-orphans 옵션으로 불필요한 컨테이너 정리
docker-compose up -d --remove-orphans

echo "🧹 [3/3] 최적화: 사용하지 않는 구형 이미지를 정리합니다..."
docker image prune -f

echo "✅ 배포 완료! 현재 구동 중인 컨테이너 상태:"
docker ps --filter "name=cpost_active"

echo "---------------------------------------------------------"
echo "💡 실시간 로그 확인: docker logs -f cpost_active"
echo "---------------------------------------------------------"